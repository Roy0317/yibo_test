public class JoinChatRoomJsonModel {
    private String code;
    private String source;
    private String userId;
    private String roomId;
    private String roomKey;
    private String agentUserCode;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getRoomId() {
        return roomId;
    }

    public void setRoomId(String roomId) {
        this.roomId = roomId;
    }

    public String getRoomKey() {
        return roomKey;
    }

    public void setRoomKey(String roomKey) {
        this.roomKey = roomKey;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getAgentUserCode() {
        return agentUserCode;
    }

    public void setAgentUserCode(String agentUserCode) {
        this.agentUserCode = agentUserCode;
    }

    public JoinChatRoomJsonModel(String code, String source, String userId, String roomId, String roomKey) {
        this.code = code;
        this.source = source;
        this.userId = userId;
        this.roomId = roomId;
        this.roomKey = roomKey;
    }

}
