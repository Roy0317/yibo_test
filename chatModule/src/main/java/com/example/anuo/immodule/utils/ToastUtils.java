public class ToastUtils {
    private static Toast mToast;

    public static void showToast(final Context mContext, final String text, final int duration) {
        ((Activity) mContext).runOnUiThread(new Runnable() {
            @Override
            public void run() {
                if (mToast != null) {
                    mToast.setText(text);
                    mToast.setDuration(duration);
                } else {
                    mToast = Toast.makeText(mContext, text, duration);
                }
                mToast.show();
            }
        });
    }

    public static void showToast(Context mContext, String s) {
        showToast(mContext, s, Toast.LENGTH_SHORT);
    }

    public static void showToast(Context context, int str) {
        showToast(context, context.getResources().getString(str));
    }
}
