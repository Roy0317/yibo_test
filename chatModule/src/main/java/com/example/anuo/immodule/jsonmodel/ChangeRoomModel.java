public class ChangeRoomModel {
    private String oldRoomId;
    private String newRoomId;
    private String stationId;
    private String nickName;
    private String source;
    private String account;


    public ChangeRoomModel(String oldRoomId, String newRoomId, String stationId, String nickName, String source,String account) {
        this.oldRoomId = oldRoomId;
        this.newRoomId = newRoomId;
        this.stationId = stationId;
        this.nickName = nickName;
        this.source = source;
        this.account = account;
    }

    public String getOldRoomId() {
        return oldRoomId;
    }

    public void setOldRoomId(String oldRoomId) {
        this.oldRoomId = oldRoomId;
    }

    public String getNewRoomId() {
        return newRoomId;
    }

    public void setNewRoomId(String newRoomId) {
        this.newRoomId = newRoomId;
    }

    public String getStationId() {
        return stationId;
    }

    public void setStationId(String stationId) {
        this.stationId = stationId;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }
}
