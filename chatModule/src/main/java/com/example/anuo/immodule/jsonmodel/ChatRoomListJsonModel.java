public class ChatRoomListJsonModel {
    private String code;
    private String source;
    private String userId;
    private String stationId;
    private String agentUser;//是否是代理线用户
    private String switchAgentEnterPublicRoom;//代理线用户是否能进入公共房间

    public ChatRoomListJsonModel(String code, String source, String userId, String stationId, String agentUser, String switchAgentEnterPublicRoom) {
        this.code = code;
        this.source = source;
        this.userId = userId;
        this.stationId = stationId;
        this.agentUser = agentUser;
        this.switchAgentEnterPublicRoom = switchAgentEnterPublicRoom;
    }

    public String getCode() {

        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getStationId() {
        return stationId;
    }

    public void setStationId(String stationId) {
        this.stationId = stationId;
    }

    public String getAgentUser() {
        return agentUser;
    }

    public void setAgentUser(String agentUser) {
        this.agentUser = agentUser;
    }

    public String getSwitchAgentEnterPublicRoom() {
        return switchAgentEnterPublicRoom;
    }

    public void setSwitchAgentEnterPublicRoom(String switchAgentEnterPublicRoom) {
        this.switchAgentEnterPublicRoom = switchAgentEnterPublicRoom;
    }
}
