public class CommonEvent {
    // 事件标记
    private String tag;

    private Object data;

    public CommonEvent(String tag) {
        this.tag = tag;
    }

    public CommonEvent(String tag, Object data) {
        this.tag = tag;
        this.data = data;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }
}
