public class AnimateUtil {
    /**
     * 设置view的旋转动画
     *
     * @param ctx
     * @param view
     */
    public static void startRotateAnim(Context ctx, View view) {
        Animation rotate = AnimationUtils.loadAnimation(ctx, R.anim.rotate_anim);
        rotate.setInterpolator(new LinearInterpolator());//不停顿
        view.setAnimation(rotate);
        view.startAnimation(rotate);
    }
}
