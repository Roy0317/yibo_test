public class WinningDataMF extends MarqueeFactory<LinearLayout, WinningDataWraper> {
    private final LayoutInflater inflater;

    public WinningDataMF(Context context) {
        super(context);
        inflater = LayoutInflater.from(mContext);
    }

    @Override
    protected LinearLayout generateMarqueeItemView(WinningDataWraper data) {
        LinearLayout linearLayout = (LinearLayout) inflater.inflate(R.layout.item_winning_data, null);
        ((TextView) linearLayout.findViewById(R.id.tv_name)).setText(data.getAccount());
        ((TextView) linearLayout.findViewById(R.id.tv_winning_money)).setText(data.getWinMoney() + "元");
        ((TextView) linearLayout.findViewById(R.id.tv_lottery_name)).setText(data.getProductName());
        return linearLayout;
    }
}
